    // Método para mover el botón a una posición aleatoria dentro del JPanel
    private void moverBtn() {
        int panelWidth = jPanel1.getWidth() - jButton2.getWidth();
        int panelHeight = jPanel1.getHeight() - jButton2.getHeight();

        int x = random.nextInt(panelWidth);
        int y = random.nextInt(panelHeight);

        jButton2.setLocation(x, y);
    }
    
